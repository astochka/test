# Функція для анімації змійки
def snake_animation():
    snake = [
        ">>>", ">>>>", ">>>>>", ">>>>>>", ">>>>>>>", ">>>>>>>>", ">>>>>>>>>",
        ">>>>>>>>", ">>>>>>>>>", ">>>>>>>>", ">>>>>>>>>", ">>>>>>>>>"
    ]
    for frame in snake:
        sys.stdout.write("\r" + frame)
        sys.stdout.flush()
        time.sleep(0.2)  # Затримка між кадрами

