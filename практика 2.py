# Метод strip(): Видаляє пробіли та зайві символи з країв рядка
text = "   Привіт, світ!   \n"
clean_text = text.strip()
print(f"Після strip(): '{clean_text}'")
#Навіщо: Коли потрібно очистити введені дані або текст із файлів.

# Метод split(): Розбиває рядок на список за вказаним роздільником
fruits = "яблуко, банан, вишня"
fruits_list = fruits.split(", ")
print(f"Після split(): {fruits_list}")
#Навіщо: Коли треба перетворити текст на окремі частини, наприклад, зі списку чи таблиці.

#Чому важливо:Ці методи спрощують роботу з текстом, роблять код чистішим і допомагають уникнути помилок із зайвими символами чи неправильним форматом даних.